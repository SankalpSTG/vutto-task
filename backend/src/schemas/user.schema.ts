import mongoose from "mongoose"
import { COLLECTIONS } from "../constants/collections"

export const UserSchema = new mongoose.Schema({
    name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        unique: true,
        required: true
    },
    phoneNumber: {
        type: String,
        required: true
    },
    password: {
        type: String,
        required: true
    },
    createdAt: {
        type: Date,
        default: () => new Date()
    },
    updatedAt: {
        type: Date,
        default: () => new Date()
    }
}, {
    collection: COLLECTIONS.users
})

export const UserModel = mongoose.model(COLLECTIONS.users, UserSchema)